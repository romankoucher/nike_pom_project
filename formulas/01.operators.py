# | Запись    | Полная форма | Что делает            |
# | --------- | ------------ | --------------------- |
# | `x += y`  | `x = x + y`  | прибавляет            |
# | `x -= y`  | `x = x - y`  | вычитает              |
# | `x *= y`  | `x = x * y`  | умножает              |
# | `x /= y`  | `x = x / y`  | делит                 |
# | `x //= y` | `x = x // y` | целочисленное деление |
# | `x %= y`  | `x = x % y`  | остаток от деления    |
# | `x **= y` | `x = x ** y` | возведение в степень  |
